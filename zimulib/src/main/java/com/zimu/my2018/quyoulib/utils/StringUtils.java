    /**
     * 将一个InputStream流转换成字符串
     *
     * @param is
     * @return
     */
    public static String toConvertString(InputStream is) {
        StringBuffer res = new StringBuffer();
        InputStreamReader isr = new InputStreamReader(is);
        BufferedReader read = new BufferedReader(isr);
        try {
            String line;
            line = read.readLine();
            while (line != null) {
                res.append(line + "<br>");
                line = read.readLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (null != isr) {
                    isr.close();
                    isr.close();
                }
                if (null != read) {
                    read.close();
                }
                if (null != is) {
                    is.close();
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
        return res.toString();
    }

    /***
     * 截取字符串
     * @param start 从那里开始，0算起
     * @param num   截取多少个
     * @param str   截取的字符串
     * @return
     */
    public static String getSubString(int start, int num, String str) {
        if (str == null) {
            return "";
        }
        int leng = str.length();
        if (start < 0) {
            start = 0;
        }
        if (start > leng) {
            start = leng;
        }
        if (num < 0) {
            num = 1;
        }
        int end = start + num;
        if (end > leng) {
            end = leng;
        }
        return str.substring(start, end);
    }

    /**
     * 判断String是否为空
     *
     * @param data
     * @return
     */
    public static boolean checkNullPoint(String data) {
        return !("null".equals(data) || "".equals(data) || "  ".equals(data) || "(null)".equals(data) || null == data
                || data.isEmpty() || "<null>".equals(data) || "NULL".equals(data));
    }

    /**
     * 获取空字符
     *
     * @param data
     * @return
     */
    public static String getNotNullStr(String data) {
        if (!checkNullPoint(data)) {
            data = "";
        }
        return data;
    }

    /**
     * 用分隔符segStr 连接2个字符串
     *
     * @param firStr
     * @param secStr
     * @param segStr
     * @return
     */
    public static String connectTwoString(String firStr, String secStr, String segStr) {
        String showMsg = "";
        if (StringUtils.checkNullPoint(firStr)) {
            showMsg = firStr;
        }

        if (StringUtils.checkNullPoint(secStr)) {
            if (showMsg.isEmpty()) {
                showMsg = secStr;
            } else {
                showMsg = showMsg + segStr + secStr;
            }
        }
        return showMsg;
    }
